/* Helper: get random item from array */
function vyvyRandomItem(arr) {
  if (!arr || arr.length === 0) return null;
  return arr[Math.floor(Math.random() * arr.length)];
}

/* Helper: get random starter for session mode */
function vyvyGetStarter(mode) {
  switch (mode) {
    case 'story':
      var story = vyvyRandomItem(VYVY_CONTENT.stories);
      return story ? story.starter : null;
    case 'quiz':
      return vyvyRandomItem(VYVY_CONTENT.math);
    case 'english':
      var eng = vyvyRandomItem(VYVY_CONTENT.english);
      return eng ? eng.practice : null;
    case 'math':
      var mathItem = vyvyRandomItem(VYVY_CONTENT.math);
      return mathItem ? mathItem.q + ' (' + mathItem.hint + ')' : null;
    case 'imagination':
      return vyvyRandomItem(VYVY_CONTENT.imagination);
    case 'feelings':
      return vyvyRandomItem(VYVY_CONTENT.feelings);
    case 'bedtime':
      return vyvyRandomItem(VYVY_CONTENT.bedtime);
    case 'talk':
    case 'free_chat':
    default:
      return vyvyRandomItem(VYVY_CONTENT.free_talk);
  }
}
